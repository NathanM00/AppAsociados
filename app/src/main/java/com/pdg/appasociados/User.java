package com.pdg.appasociados;

public class User {
    String nombre;
    String correo;
    String contra;
    Boolean asociado;

    public void User(){

    }

    public User(String nombre, String correo, String contra, Boolean asociado) {
        this.nombre = nombre;
        this.correo = correo;
        this.contra = contra;
        this.asociado = asociado;

    }


}

